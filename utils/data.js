// 通过名称反查 Hero 对象
const findHeroByName = (name) => {
  if (!name) return null;
  const allHeroes = [...HERO_DATA.tank, ...HERO_DATA.damage, ...HERO_DATA.support];
  let hero = allHeroes.find(h => h.name === name);
  if (!hero) {
    hero = allHeroes.find(h => name.includes(h.name) || h.name.includes(name));
  }
  return hero;
};

// 获取英雄职业
const getHeroRole = (heroId) => {
  if (HERO_DATA.tank.find(h => h.id === heroId)) return 'tank';
  if (HERO_DATA.damage.find(h => h.id === heroId)) return 'damage';
  if (HERO_DATA.support.find(h => h.id === heroId)) return 'support';
  return null;
};

